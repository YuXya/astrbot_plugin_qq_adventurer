def load_builtin_world_book() -> str:
    path = (
        Path(__file__).resolve().parents[1]
        / "world_book"
        / "books"
        / "default.json"
    )
    try:
        return path.read_text(encoding="utf-8")
    except Exception:
        return json.dumps({"version": 1, "entries": []}, ensure_ascii=False, indent=2)


